                setState(() => _selectedGolonganDarah = value);
              }),
              const SizedBox(height: 16),
              _buildDropdownField('Peran Keluarga', _selectedPeranKeluarga,
                  _peranKeluargaOptions, (value) {
                setState(() => _selectedPeranKeluarga = value);
              }),
